import UIKit

extension UIColor {
    
    open class var sdkBlack: UIColor {
        return UIColor(hex: "151522")
    }
    
    open class var sdkGreen: UIColor {
        return UIColor(hex: "68BE0A")
    }
    
    open class var appGray: UIColor {
        return UIColor(hex: "323247")
    }
    
}
